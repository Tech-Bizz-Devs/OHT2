    private void jLabel20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel20MouseClicked
        jLabel20.setText(""); // Clear any existing text in the label

        JFileChooser jfc = new JFileChooser();
        int result = jfc.showOpenDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = jfc.getSelectedFile();

            // Check if the selected file is an image (you may need to add more file type checks)
            if (selectedFile.isFile() && (selectedFile.getName().toLowerCase().endsWith(".jpg")
                    || selectedFile.getName().toLowerCase().endsWith(".jpeg")
                    || selectedFile.getName().toLowerCase().endsWith(".png")
                    || selectedFile.getName().toLowerCase().endsWith(".gif"))) {

                String filename = selectedFile.getAbsolutePath();
                System.out.println(filename);
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(jLabel20.getWidth(), jLabel20.getHeight(), Image.SCALE_DEFAULT));
                jLabel20.setIcon(imageIcon);
                try {
                   fin = new FileInputStream(selectedFile.getAbsoluteFile());
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                // Handle the case where the selected file is not an image
                jLabel20.setText("Selected file is not an image.");
            }
        }

    }//GEN-LAST:event_jLabel20MouseClicked

